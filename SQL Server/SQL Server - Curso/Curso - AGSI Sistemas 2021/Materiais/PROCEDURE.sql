--DROP PROCEDURE PSOMA
CREATE OR ALTER PROCEDURE PSOMA (
  @SOMA NUMERIC OUTPUT 
)AS
BEGIN
  SET @SOMA = (1 + 1)
END
GO

--USO
BEGIN
  DECLARE @RET NUMERIC
  EXEC PSOMA @RET OUTPUT
  PRINT @RET
END









--DROP PROCEDURE PSOMAR2
CREATE OR ALTER PROCEDURE PSOMAR2(
  @N1 NUMERIC,
  @N2 NUMERIC,
  @SOMA NUMERIC OUTPUT 
)AS
BEGIN
  SET @SOMA = (@N1 + @N2)
  PRINT '1º Nº = '+CAST(@N1 AS VARCHAR)
  PRINT '2º Nº = '+CAST(@N2 AS VARCHAR)
END
GO

BEGIN
  DECLARE @RET NUMERIC
  DECLARE @N1 NUMERIC = 10
  DECLARE @N2 NUMERIC = 20

  EXEC PSOMAR2 @N1,
                @N2,
                @RET OUT
  PRINT '--------------'
  PRINT 'SOMA = '+CAST(@RET AS VARCHAR)
END










